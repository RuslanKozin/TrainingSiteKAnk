<?php
function area($width, $height) {  /*function - сообщаем, что объявляем новую функцию*/ /*area - имя функции*/
                                  /*В скобках после имени функции записываются аргументы, которые она принимает*/
            /*При объявлении собственной функции вместо значений аргументов указывают имена переменных.*/
    return $width * $height;  /*команда return позволяет мгновенно перейти из функции в главный скрипт.*/
}